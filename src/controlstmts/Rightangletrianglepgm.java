package controlstmts;

public class Rightangletrianglepgm {

	public static void main(String[] args) {
		
		//right angle triangle with a number which repeats a number in a row
		
		 int n=4;
		for(int i=1;i<=n;i++)
		 {
			for(int j=1;j<=i;j++)
			{
			
				System.out.print(i);
			}
		
			System.out.println();
		 }
	}

}
